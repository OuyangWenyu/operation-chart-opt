% --- Executes on key press with focus on figure1 or any of its controls.
function figure1_WindowKeyPressFcn(hObject, eventdata, handles)
% hObject    handle to figure1 (see GCBO)
% eventdata  structure with the following fields (see FIGURE)
%	Key: name of the key that was pressed, in lower case
%	Character: character interpretation of the key(s) that was pressed
%	Modifier: name(s) of the modifier key(s) (i.e., control, shift) pressed
% handles    structure with handles and user data (see GUIDATA) 4
p=get(gca,'CurrentPoint');
switch get(gcf,'CurrentKey')
    case('space')
        zoom off
        pan off
        handles.cpx=p(end,1);
        handles.cpy=p(end,2);   
        handles.idata(end+1,:)=[handles.cpx,handles.cpy];
    case('delete')
        temp=handles.idata;
        clear handles.idata;
        if size(temp,1)>2
            handles.idata=temp(1:end-1,:);
        else 
            handles.idata=zeros(2,2);
        end
        handles.cpx=handles.idata(end,1);
        handles.cpy=handles.idata(end,2); 
end
guidata(hObject, handles);
h=findobj('tag','edit7');
set(h,'String',num2str(size(handles.idata,1)));
hold on
plot(handles.cpx,handles.cpy,'b*');


