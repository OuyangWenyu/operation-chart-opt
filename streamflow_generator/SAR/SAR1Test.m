n=input('Input the number of periods in a year,12/36[12]: ');
m=input('Input the number of simulation years: ');
kesi=randn(m,n);
path= input('Input path of the file: ','s');
sheet= input('Input sheet of the excel: ','s');
X=readData(path,sheet);
Z=WHReverse(X);
rou_1=autoCorrSeason(Z);
fai_1=rou_1;
var_epsilon=1-fai_1.*rou_1;
std_epsilon=var_epsilon.^(1/2);
epsilon_t_tau=bsxfun(@times,kesi,std_epsilon);
z=SAR1(m,n,fai_1,epsilon_t_tau);
CsX=skewnessCoef(X);
y=WH(z,CsX);
muX=mean(X);
stdX= std(X);
x=standReverse(y,muX,stdX);
pathOut= input('Output path of the result: ','s');
xlswrite(pathOut,x);